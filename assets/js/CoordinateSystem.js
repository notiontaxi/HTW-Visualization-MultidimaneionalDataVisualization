window.CoordinateSystem = CoordinateSystem; 
"use strict";

  function CoordinateSystem(canvas){
    this.canvas = canvas;
  }


  CoordinateSystem.prototype.meashured = function(xVals, yVals){
    // use:
    xVal.min
    xVal.max
    yVal.min
    yVal.max
    canvas.drawLine()
  }

  CoordinateSystem.prototype.drawLines = function(){
    // use:
    startPoint = {
        x: 0
      , y: 0
    }
    endPoint = {
        x: 0
      , y: 0
    }
    canvas.drawLine(startPoint, endPoint)
  }  




